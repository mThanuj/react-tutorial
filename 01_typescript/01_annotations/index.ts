// Annotations are used to specify the datatype of a variable for type safety
//

// Strings
let myName: string = "Thanuj";
myName = "Mullaguri";
// myName = 21 // error
console.log(myName);

// Numbers
let age: number = 20;
age = 21;
// age = true // error
console.log(age);

// Boolean
let isEligible: boolean = true;
isEligible = false;
// isEligible = 21 // error
console.log(isEligible);
